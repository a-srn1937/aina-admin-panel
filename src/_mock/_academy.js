// Helper function to get all lessons from a roadmap
export function getAllLessonsFromRoadmap(roadmapId) {
  const roadmap = _roadmaps.find((r) => r.id === roadmapId);
  if (!roadmap) return [];

  const lessons = [];
  roadmap.courses.forEach((course) => {
    course.modules.forEach((module) => {
      module.lessons.forEach((lesson) => {
        lessons.push({
          ...lesson,
          moduleName: module.title,
          courseName: course.title,
        });
      });
    });
  });
  return lessons;
}

// Helper function to get first lesson of a course
export function getFirstLessonOfCourse(courseId) {
  for (const roadmap of _roadmaps) {
    const course = roadmap.courses.find((c) => c.id === courseId);
    if (course && course.modules[0]?.lessons[0]) {
      return course.modules[0].lessons[0];
    }
  }
  return null;
}
